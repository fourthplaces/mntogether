#!/usr/bin/env node

// seed.mjs — Reads JSON seed files and outputs idempotent SQL to stdout.
//
// Usage:
//   node data/seed.mjs | docker compose exec -T postgres psql -U postgres -d rooteditorial
//
// Reads: data/tags.json, data/organizations.json, data/posts.json
// Zero external dependencies (Node stdlib only).

import { readFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const read = (f) => JSON.parse(readFileSync(join(__dirname, f), "utf8"));

const tags = read("tags.json");
const orgs = read("organizations.json");
const posts = read("posts.json");

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const esc = (s) => {
  if (s == null) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
};

const out = (line) => process.stdout.write(line + "\n");

// ---------------------------------------------------------------------------
// SQL Generation
// ---------------------------------------------------------------------------

out("-- Generated by data/seed.mjs");
out("-- Source: tags.json, organizations.json, posts.json");
out("BEGIN;");
out("");

// --- Tags ------------------------------------------------------------------

out("-- Tags: topic");
for (const t of tags.topic) {
  out(
    `INSERT INTO tags (kind, value, display_name, color) VALUES ('topic', ${esc(t.value)}, ${esc(t.displayName)}, ${esc(t.color)}) ON CONFLICT (kind, value) DO UPDATE SET color = EXCLUDED.color, display_name = EXCLUDED.display_name;`
  );
}
out("");

out("-- Tags: service_area");
for (const t of tags.serviceArea) {
  out(
    `INSERT INTO tags (kind, value, display_name) VALUES ('service_area', ${esc(t.value)}, ${esc(t.displayName)}) ON CONFLICT (kind, value) DO NOTHING;`
  );
}
out("");

out("-- Tags: safety");
for (const t of tags.safety) {
  out(
    `INSERT INTO tags (kind, value, display_name) VALUES ('safety', ${esc(t.value)}, ${esc(t.displayName)}) ON CONFLICT (kind, value) DO NOTHING;`
  );
}
out("");

// Ensure reserved tags exist (seeded by migration 000173 but guard anyway)
out("-- Tags: reserved (guard)");
const reservedTags = [
  ["urgent", "Urgent"],
  ["recurring", "Recurring"],
  ["need", "Need"],
  ["aid", "Aid"],
  ["action", "Action"],
  ["person", "Person"],
  ["business", "Business"],
  ["closed", "Closed"],
];
for (const [val, display] of reservedTags) {
  out(
    `INSERT INTO tags (kind, value, display_name) VALUES ('reserved', ${esc(val)}, ${esc(display)}) ON CONFLICT (kind, value) DO NOTHING;`
  );
}
out("");

// --- Organizations ---------------------------------------------------------

out("-- Organizations");
// organizations.json may be { "organizations": [...] } or a flat array
const orgEntries = Array.isArray(orgs)
  ? orgs
  : orgs.organizations || Object.values(orgs).flat();

for (const org of orgEntries) {
  const name = org.name || org.organization;
  const desc = org.description || org.populations_served || null;
  out(
    `INSERT INTO organizations (name, description, status) VALUES (${esc(name)}, ${esc(desc)}, 'approved') ON CONFLICT (name) DO NOTHING;`
  );
}
out("");

// --- Posts -----------------------------------------------------------------

out("-- Posts");
let postIndex = 0;
for (const p of posts) {
  const alias = `p${postIndex++}`;

  // Collect all tag clauses for this post
  const tagClauses = [];
  if (p.tags) {
    for (const [kind, values] of Object.entries(p.tags)) {
      const dbKind = kind === "serviceArea" ? "service_area" : kind;
      for (const val of values) {
        tagClauses.push(`(t.kind = ${esc(dbKind)} AND t.value = ${esc(val)})`);
      }
    }
  }

  // Begin CTE
  out(`-- ${p.postType}: ${p.title}`);
  out(`WITH post AS (`);
  out(`    INSERT INTO posts (title, description, summary, post_type, category, weight, priority, status, submission_type, published_at, location)`);
  out(`    VALUES (`);
  out(`        ${esc(p.title)},`);
  out(`        ${esc(p.description)},`);
  out(`        ${esc(p.summary)},`);
  out(`        ${esc(p.postType)}, ${esc(p.category)}, ${esc(p.weight)}, ${p.priority}, 'active', 'admin', now(), ${esc(p.location || null)}`);
  out(`    )`);
  out(`    ON CONFLICT DO NOTHING RETURNING id`);
  out(`)`);

  // Meta
  if (p.meta) {
    const kicker = p.meta.kicker || null;
    const timestamp = p.meta.timestamp || null;
    if (kicker || timestamp) {
      out(`, meta AS (`);
      out(`    INSERT INTO post_meta (post_id, kicker, timestamp)`);
      out(`    SELECT id, ${esc(kicker)}, ${timestamp ? esc(timestamp) : "NULL"} FROM post`);
      out(`    ON CONFLICT (post_id) DO NOTHING`);
      out(`)`);
    }
  }

  // Source
  if (p.source) {
    out(`, src AS (`);
    out(`    INSERT INTO post_source_attribution (post_id, source_name)`);
    out(`    SELECT id, ${esc(p.source)} FROM post`);
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
  }

  // Person (spotlights)
  if (p.person) {
    out(`, person AS (`);
    out(`    INSERT INTO post_person (post_id, name, role, bio, photo_url, quote)`);
    out(
      `    SELECT id, ${esc(p.person.name)}, ${esc(p.person.role)}, ${esc(p.person.bio)}, ${esc(p.person.photoUrl)}, ${esc(p.person.quote)} FROM post`
    );
    out(`    ON CONFLICT DO NOTHING`);
    out(`)`);
  }

  // Schedule (events)
  if (p.schedule && p.schedule.length > 0) {
    for (let si = 0; si < p.schedule.length; si++) {
      const s = p.schedule[si];
      out(`, sched${si} AS (`);
      out(
        `    INSERT INTO schedules (schedulable_type, schedulable_id, dtstart, dtend, rrule, is_all_day, day_of_week, opens_at, closes_at)`
      );
      out(
        `    SELECT 'post', id, ${s.dtstart ? esc(s.dtstart) + "::timestamptz" : "NULL"}, ${s.dtend ? esc(s.dtend) + "::timestamptz" : "NULL"}, ${esc(s.rrule || null)}, ${s.isAllDay ?? false}, ${s.dayOfWeek ?? "NULL"}, ${s.opensAt ? esc(s.opensAt) + "::time" : "NULL"}, ${s.closesAt ? esc(s.closesAt) + "::time" : "NULL"} FROM post`
      );
      out(`    ON CONFLICT DO NOTHING`);
      out(`)`);
    }
  }

  // Items (references)
  if (p.items && p.items.length > 0) {
    for (let ii = 0; ii < p.items.length; ii++) {
      const item = p.items[ii];
      out(`, item${ii} AS (`);
      out(`    INSERT INTO post_items (post_id, name, detail, sort_order)`);
      out(
        `    SELECT id, ${esc(item.name)}, ${esc(item.detail || null)}, ${item.sortOrder ?? ii} FROM post`
      );
      out(`    ON CONFLICT DO NOTHING`);
      out(`)`);
    }
  }

  // Links
  if (p.links && p.links.length > 0) {
    for (let li = 0; li < p.links.length; li++) {
      const link = p.links[li];
      out(`, link${li} AS (`);
      out(`    INSERT INTO post_link (post_id, url, label, deadline)`);
      out(
        `    SELECT id, ${esc(link.url)}, ${esc(link.label || null)}, ${link.deadline ? esc(link.deadline) + "::date" : "NULL"} FROM post`
      );
      out(`    ON CONFLICT (post_id) DO NOTHING`);
      out(`)`);
    }
  }

  // Media
  if (p.media && p.media.length > 0) {
    for (let mi = 0; mi < p.media.length; mi++) {
      const m = p.media[mi];
      out(`, media${mi} AS (`);
      out(
        `    INSERT INTO post_media (post_id, image_url, caption, credit, sort_order)`
      );
      out(
        `    SELECT id, ${esc(m.imageUrl)}, ${esc(m.caption || "")}, ${esc(m.credit || "")}, ${m.sortOrder ?? mi} FROM post`
      );
      out(`    ON CONFLICT DO NOTHING`);
      out(`)`);
    }
  }

  // Taggables — final SELECT in the CTE chain
  if (tagClauses.length > 0) {
    out(`INSERT INTO taggables (tag_id, taggable_type, taggable_id)`);
    out(`SELECT t.id, 'post', post.id FROM post CROSS JOIN tags t`);
    out(`WHERE ${tagClauses.join("\n   OR ")}`);
    out(`ON CONFLICT DO NOTHING;`);
  } else {
    // No tags — just execute the CTE
    out(`SELECT id FROM post;`);
  }
  out("");
}

// --- Sources & Post-Sources (wire orgs → posts) ----------------------------
// The organizations query (find_approved) joins organizations → sources →
// post_sources → posts.  We create one "seed" source per org, then link each
// post whose post_source_attribution.source_name matches an org name.

out("-- Sources: one per organization");
out(`INSERT INTO sources (source_type, organization_id, status, active)
SELECT 'seed', o.id, 'approved', true
FROM organizations o
WHERE NOT EXISTS (
    SELECT 1 FROM sources s WHERE s.organization_id = o.id
);`);
out("");

out("-- Post-Sources: link posts to their org's source via source_name match");
out(`INSERT INTO post_sources (post_id, source_type, source_id)
SELECT psa.post_id, 'seed', s.id
FROM post_source_attribution psa
JOIN organizations o ON o.name = psa.source_name
JOIN sources s ON s.organization_id = o.id AND s.source_type = 'seed'
WHERE NOT EXISTS (
    SELECT 1 FROM post_sources ps
    WHERE ps.post_id = psa.post_id AND ps.source_id = s.id
);`);
out("");

out("COMMIT;");
out("-- Done.");
