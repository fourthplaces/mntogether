#!/usr/bin/env node

// seed.mjs — Reads JSON seed files and outputs idempotent SQL to stdout.
//
// Usage:
//   node data/seed.mjs | docker compose exec -T postgres psql -U postgres -d rooteditorial
//
// Reads: data/tags.json, data/organizations.json, data/posts.json
// Zero external dependencies (Node stdlib only).

import { readFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const read = (f) => JSON.parse(readFileSync(join(__dirname, f), "utf8"));

const tags = read("tags.json");
const orgs = read("organizations.json");
const rawPosts = read("posts.json");
// Filter out comment-only entries
const posts = rawPosts.filter((p) => !p._comment);

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const esc = (s) => {
  if (s == null) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
};

const out = (line) => process.stdout.write(line + "\n");

/**
 * Convert an offset-days value to a SQL expression relative to NOW().
 * Positive = future, negative = past, zero = now.
 */
const offsetToSql = (days) => {
  if (days == null) return "NULL";
  if (days === 0) return "NOW()";
  if (days > 0) return `NOW() + INTERVAL '${days} days'`;
  return `NOW() - INTERVAL '${Math.abs(days)} days'`;
};

/**
 * Convert an offset-days to a SQL date expression (date only, no time).
 */
const offsetToDateSql = (days) => {
  if (days == null) return "NULL";
  if (days === 0) return "(NOW())::date";
  if (days > 0) return `(NOW() + INTERVAL '${days} days')::date`;
  return `(NOW() - INTERVAL '${Math.abs(days)} days')::date`;
};

// ---------------------------------------------------------------------------
// SQL Generation
// ---------------------------------------------------------------------------

out("-- Generated by data/seed.mjs");
out("-- Source: tags.json, organizations.json, posts.json");
out("BEGIN;");
out("");
out("-- Set timezone for all date computations");
out("SET LOCAL timezone = 'America/Chicago';");
out("");

// --- Tags ------------------------------------------------------------------

out("-- Tags: topic");
for (const t of tags.topic) {
  out(
    `INSERT INTO tags (kind, value, display_name, color) VALUES ('topic', ${esc(t.value)}, ${esc(t.displayName)}, ${esc(t.color)}) ON CONFLICT (kind, value) DO UPDATE SET color = EXCLUDED.color, display_name = EXCLUDED.display_name;`
  );
}
out("");

out("-- Tags: service_area");
for (const t of tags.serviceArea) {
  out(
    `INSERT INTO tags (kind, value, display_name) VALUES ('service_area', ${esc(t.value)}, ${esc(t.displayName)}) ON CONFLICT (kind, value) DO NOTHING;`
  );
}
out("");

out("-- Tags: safety");
for (const t of tags.safety) {
  out(
    `INSERT INTO tags (kind, value, display_name) VALUES ('safety', ${esc(t.value)}, ${esc(t.displayName)}) ON CONFLICT (kind, value) DO NOTHING;`
  );
}
out("");

// --- Organizations ---------------------------------------------------------

out("-- Organizations");
const orgEntries = Array.isArray(orgs)
  ? orgs
  : orgs.organizations || Object.values(orgs).flat();

for (const org of orgEntries) {
  const name = org.name || org.organization;
  const desc = org.description || org.populations_served || null;
  out(
    `INSERT INTO organizations (name, description, status) VALUES (${esc(name)}, ${esc(desc)}, 'approved') ON CONFLICT (name) DO NOTHING;`
  );
}
out("");

// --- Posts -----------------------------------------------------------------

// Clean up any previous seed run. Delete all ingested posts (seed data)
// so re-running the seed is idempotent. CASCADE via FK handles field group rows.
out("-- Clean previous seed data (ingested posts only)");
out("DELETE FROM edition_slots WHERE post_id IN (SELECT id FROM posts WHERE submission_type = 'ingested');");
out("DELETE FROM posts WHERE submission_type = 'ingested';");
out("");

out("-- Posts");
let postIndex = 0;
for (const p of posts) {
  const alias = `p${postIndex}`;
  postIndex++;

  // Collect all tag clauses for this post
  const tagClauses = [];
  if (p.tags) {
    for (const [kind, values] of Object.entries(p.tags)) {
      const dbKind = kind === "serviceArea" ? "service_area" : kind;
      for (const val of values) {
        tagClauses.push(`(t.kind = ${esc(dbKind)} AND t.value = ${esc(val)})`);
      }
    }
  }

  // Compute published_at from offset
  const publishedAt = offsetToSql(p.publishedOffsetDays ?? 0);

  // Determine body_raw — fall back through body tiers
  const bodyRaw = p.bodyRaw || p.bodyHeavy || p.bodyMedium || p.bodyLight || "";

  // Submission type
  const submissionType = p.submissionType || "admin";

  // Begin CTE
  out(`-- ${p.postType}: ${p.title}`);
  out(`WITH post AS (`);
  out(
    `    INSERT INTO posts (title, body_raw, body_heavy, body_medium, body_light, post_type, weight, priority, status, submission_type, published_at, location, zip_code, is_urgent)`
  );
  out(`    VALUES (`);
  out(`        ${esc(p.title)},`);
  out(`        ${esc(bodyRaw)},`);
  out(`        ${esc(p.bodyHeavy || null)},`);
  out(`        ${esc(p.bodyMedium || null)},`);
  out(`        ${esc(p.bodyLight || null)},`);
  out(
    `        ${esc(p.postType)}, ${esc(p.weight)}, ${p.priority}, 'active', ${esc(submissionType)}, ${publishedAt}, ${esc(p.location || null)}, ${esc(p.zipCode || null)}, ${p.is_urgent ? "true" : "false"}`
  );
  out(`    )`);
  out(`    ON CONFLICT DO NOTHING RETURNING id`);
  out(`)`);

  // Track CTE aliases for chaining
  let lastAlias = "post";
  let cteIndex = 0;
  const nextAlias = (prefix) => {
    const a = `${prefix}${cteIndex++}`;
    return a;
  };

  // --- post_meta -----------------------------------------------------------
  if (p.meta) {
    const a = nextAlias("meta");
    const kicker = p.meta.kicker || null;
    const byline = p.meta.byline || null;
    const deck = p.meta.deck || null;
    const updated = p.meta.updated || null;
    // timestamp from offset
    const tsExpr =
      p.meta.timestampOffsetDays != null
        ? offsetToSql(p.meta.timestampOffsetDays)
        : "NULL";

    out(`, ${a} AS (`);
    out(`    INSERT INTO post_meta (post_id, kicker, byline, timestamp, updated, deck)`);
    out(
      `    SELECT id, ${esc(kicker)}, ${esc(byline)}, ${tsExpr}, ${esc(updated)}, ${esc(deck)} FROM post`
    );
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
    lastAlias = a;
  }

  // --- post_source_attribution ---------------------------------------------
  if (p.sourceAttribution) {
    const a = nextAlias("src");
    out(`, ${a} AS (`);
    out(`    INSERT INTO post_source_attribution (post_id, source_name, attribution)`);
    out(
      `    SELECT id, ${esc(p.sourceAttribution.sourceName)}, ${esc(p.sourceAttribution.attribution || null)} FROM post`
    );
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
    lastAlias = a;
  }

  // --- post_person (spotlights) --------------------------------------------
  if (p.person) {
    const a = nextAlias("person");
    out(`, ${a} AS (`);
    out(`    INSERT INTO post_person (post_id, name, role, bio, photo_url, quote)`);
    out(
      `    SELECT id, ${esc(p.person.name)}, ${esc(p.person.role)}, ${esc(p.person.bio)}, ${esc(p.person.photoUrl || null)}, ${esc(p.person.quote || null)} FROM post`
    );
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
    lastAlias = a;
  }

  // --- post_datetime (events) ----------------------------------------------
  if (p.datetime) {
    const a = nextAlias("dt");
    // Use date-only offset + explicit start time (default 10:00 AM CT)
    const startTime = p.datetime.startTime || "10:00";
    const dateExpr = offsetToDateSql(p.datetime.offsetDays ?? 0);
    const startExpr = `(${dateExpr} + TIME '${startTime}')`;
    // end = start + duration
    const endExpr =
      p.datetime.durationHours != null
        ? `${startExpr} + INTERVAL '${p.datetime.durationHours} hours'`
        : "NULL";
    const cost = p.datetime.cost || null;
    const recurring = p.datetime.recurring ? "true" : "false";

    out(`, ${a} AS (`);
    out(`    INSERT INTO post_datetime (post_id, start_at, end_at, cost, recurring)`);
    out(
      `    SELECT id, ${startExpr}, ${endExpr}, ${esc(cost)}, ${recurring} FROM post`
    );
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
    lastAlias = a;
  }

  // --- post_status (exchanges) ---------------------------------------------
  if (p.status) {
    const a = nextAlias("st");
    out(`, ${a} AS (`);
    out(`    INSERT INTO post_status (post_id, state, verified)`);
    out(
      `    SELECT id, ${esc(p.status.state)}, ${esc(p.status.verified || null)} FROM post`
    );
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
    lastAlias = a;
  }

  // --- post_schedule (business hours for spotlights/references) -------------
  if (p.scheduleEntries && p.scheduleEntries.length > 0) {
    for (let si = 0; si < p.scheduleEntries.length; si++) {
      const s = p.scheduleEntries[si];
      const a = nextAlias("sched");
      out(`, ${a} AS (`);
      out(`    INSERT INTO post_schedule (post_id, day, opens, closes, sort_order)`);
      out(
        `    SELECT id, ${esc(s.day)}, ${esc(s.opens)}, ${esc(s.closes)}, ${si} FROM post`
      );
      out(`    ON CONFLICT DO NOTHING`);
      out(`)`);
      lastAlias = a;
    }
  }

  // --- post_items (reference lists) ----------------------------------------
  if (p.items && p.items.length > 0) {
    for (let ii = 0; ii < p.items.length; ii++) {
      const item = p.items[ii];
      const a = nextAlias("item");
      out(`, ${a} AS (`);
      out(`    INSERT INTO post_items (post_id, name, detail, sort_order)`);
      out(
        `    SELECT id, ${esc(item.name)}, ${esc(item.detail || null)}, ${ii} FROM post`
      );
      out(`    ON CONFLICT DO NOTHING`);
      out(`)`);
      lastAlias = a;
    }
  }

  // --- post_link (CTAs) ----------------------------------------------------
  if (p.link) {
    const a = nextAlias("link");
    const deadlineExpr = offsetToDateSql(p.link.deadlineOffsetDays);
    out(`, ${a} AS (`);
    out(`    INSERT INTO post_link (post_id, label, url, deadline)`);
    out(
      `    SELECT id, ${esc(p.link.label)}, ${esc(p.link.url)}, ${deadlineExpr} FROM post`
    );
    out(`    ON CONFLICT (post_id) DO NOTHING`);
    out(`)`);
    lastAlias = a;
  }

  // --- post_media ----------------------------------------------------------
  if (p.media && p.media.length > 0) {
    for (let mi = 0; mi < p.media.length; mi++) {
      const m = p.media[mi];
      const a = nextAlias("media");
      out(`, ${a} AS (`);
      out(
        `    INSERT INTO post_media (post_id, image_url, caption, credit, sort_order)`
      );
      out(
        `    SELECT id, ${esc(m.imageUrl)}, ${esc(m.caption || "")}, ${esc(m.credit || "")}, ${mi} FROM post`
      );
      out(`    ON CONFLICT DO NOTHING`);
      out(`)`);
      lastAlias = a;
    }
  }

  // --- Taggables — final SELECT in the CTE chain ---------------------------
  if (tagClauses.length > 0) {
    out(`INSERT INTO taggables (tag_id, taggable_type, taggable_id)`);
    out(`SELECT t.id, 'post', post.id FROM post CROSS JOIN tags t`);
    out(`WHERE ${tagClauses.join("\n   OR ")}`);
    out(`ON CONFLICT DO NOTHING;`);
  } else {
    // No tags — just execute the CTE
    out(`SELECT id FROM post;`);
  }
  out("");
}

// --- Sources & Post-Sources (wire orgs → posts) ----------------------------
// The organizations query (find_approved) joins organizations → sources →
// post_sources → posts.  We create one "seed" source per org, then link each
// post whose post_source_attribution.source_name matches an org name.

out("-- Sources: one per organization");
out(`INSERT INTO sources (source_type, organization_id, status, active)
SELECT 'seed', o.id, 'approved', true
FROM organizations o
WHERE NOT EXISTS (
    SELECT 1 FROM sources s WHERE s.organization_id = o.id
);`);
out("");

out("-- Post-Sources: link posts to their org's source via source_name match");
out(`INSERT INTO post_sources (post_id, source_type, source_id)
SELECT psa.post_id, 'seed', s.id
FROM post_source_attribution psa
JOIN organizations o ON o.name = psa.source_name
JOIN sources s ON s.organization_id = o.id AND s.source_type = 'seed'
WHERE NOT EXISTS (
    SELECT 1 FROM post_sources ps
    WHERE ps.post_id = psa.post_id AND ps.source_id = s.id
);`);
out("");

out("COMMIT;");
out("-- Done.");
